class Solution:
    # @param A : list of integers
    # @return a list of integers
    def solve(self, A):
        if len(A) < 2:
            # empty check
            return A
        i = 0
        while i < len(A):
            temp = -1
            if (i % 2 == 0 and A[i] >= 0) or (i % 2 != 0 and A[i] < 0):
                # 1st out of place
                temp = i
            else:
                i += 1
                continue
            if A[temp] >= 0:
                # find negative and insert to i
                while temp < len(A) and A[temp] >= 0:
                    temp += 1
            else:
                # find positive and insert to i
                while temp < len(A) and A[temp] < 0:
                    temp += 1
            if temp < len(A): A.insert(i, A.pop(temp))
            i += 2
        return A


